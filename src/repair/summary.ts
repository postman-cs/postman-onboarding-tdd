import { mkdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

import type { RepairStatus } from '../types.js';

const REPAIR_MARKER_START = '<!-- postman-tdd-repair';
const REPAIR_MARKER_END = '-->';
const SUMMARY_DIR = '.postman-tdd';
const SUMMARY_PATH = join(SUMMARY_DIR, 'repair-summary.json');

export interface RepairSummary {
  attempts: number;
  blockedReason?: string;
  commitSha?: string;
  message: string;
  prNumber: number;
  schemaVersion: 1;
  status: RepairStatus;
}

export function writeRepairSummary(summary: RepairSummary): string {
  mkdirSync(SUMMARY_DIR, { recursive: true });
  writeFileSync(SUMMARY_PATH, `${JSON.stringify(summary, null, 2)}\n`, 'utf8');
  return SUMMARY_PATH;
}

export function renderRepairComment(summary: RepairSummary): string {
  const marker = `${REPAIR_MARKER_START}\n${JSON.stringify({ prNumber: summary.prNumber, schemaVersion: 1 })}\n${REPAIR_MARKER_END}`;
  const statusLabel = summary.status.toUpperCase();
  const lines = [
    marker,
    `# Postman TDD Repair (${statusLabel})`,
    '',
    `**Attempts:** ${summary.attempts}`,
    summary.commitSha ? `**Commit:** \`${summary.commitSha}\`` : '',
    summary.blockedReason ? `**Blocked reason:** \`${summary.blockedReason}\`` : '',
    '',
    summary.message,
    '',
    '_Generated by Postman Onboarding TDD Repair._'
  ].filter((line) => line !== '');
  return lines.join('\n');
}

export function isRepairComment(body: string | undefined): boolean {
  return Boolean(body?.includes(REPAIR_MARKER_START));
}
