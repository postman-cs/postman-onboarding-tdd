import { mkdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

import type { FailurePhase, RepairStatus, SignedRepairCheckpoint, RepairCheckpointPayload } from '../types.js';

const REPAIR_MARKER_START = '<!-- postman-tdd-repair';
const REPAIR_MARKER_END = '-->';
const SUMMARY_DIR = '.postman-tdd';
const SUMMARY_PATH = join(SUMMARY_DIR, 'repair-summary.json');

export interface RepairSummary {
  attemptDetails?: RepairAttemptDiagnostic[];
  attempts: number;
  blockedReason?: string;
  /**
   * D9 authoritative repair resume state embedded on the repair summary.
   * SignedRepairCheckpoint when immutable-state-signing-key is set, or a
   * bare RepairCheckpointPayload when it is not. Additive in schemaVersion 2;
   * absent on v1 summaries.
   */
  checkpointRef?: SignedRepairCheckpoint | RepairCheckpointPayload;
  commitSha?: string;
  message: string;
  prNumber: number;
  schemaVersion: 1 | 2;
  status: RepairStatus;
}

export type RepairAttemptOutcome =
  | 'local_test_failed'
  | 'oracle_failed'
  | 'oracle_passed'
  | 'provider_blocked'
  | 'provider_no_change';

export interface RepairAttemptDiagnostic {
  attempt: number;
  localTest?: {
    command?: string;
    exitCode?: number;
    status: 'failed' | 'passed' | 'skipped';
  };
  oracle?: {
    failureCount?: number;
    failures?: Array<{
      assertion?: string;
      message: string;
      method?: string;
      operationId?: string;
      path?: string;
    }>;
    phase?: FailurePhase;
    status: 'failed' | 'passed' | 'skipped';
  };
  outcome: RepairAttemptOutcome;
  patchSummary?: string;
  providerStatus: 'blocked' | 'changed' | 'no_change';
  touchedPaths: string[];
}

export function writeRepairSummary(summary: RepairSummary): string {
  mkdirSync(SUMMARY_DIR, { recursive: true });
  writeFileSync(SUMMARY_PATH, `${JSON.stringify(summary, null, 2)}\n`, 'utf8');
  return SUMMARY_PATH;
}

export function renderRepairComment(summary: RepairSummary): string {
  const marker = `${REPAIR_MARKER_START}\n${JSON.stringify({ prNumber: summary.prNumber, schemaVersion: 1 })}\n${REPAIR_MARKER_END}`;
  const statusLabel = summary.status.toUpperCase();
  const lines = [
    marker,
    `# Postman TDD Repair (${statusLabel})`,
    '',
    `**Attempts:** ${summary.attempts}`,
    summary.commitSha ? `**Commit:** \`${summary.commitSha}\`` : '',
    summary.blockedReason ? `**Blocked reason:** \`${summary.blockedReason}\`` : '',
    '',
    summary.message,
    '',
    ...repairAttemptTimeline(summary),
    '',
    ...repairNextAction(summary),
    '',
    '_Generated by Postman Onboarding TDD Repair._'
  ].filter((line) => line !== '');
  return lines.join('\n');
}

function repairAttemptTimeline(summary: RepairSummary): string[] {
  const attempts = summary.attemptDetails || [];
  if (attempts.length === 0) return [];
  return [
    '## Attempt Timeline',
    '',
    '| Attempt | Patch | Paths | Local test | Oracle | Outcome |',
    '| --- | --- | --- | --- | --- | --- |',
    ...attempts.slice(0, 10).map((attempt) => [
      String(attempt.attempt),
      escapeTableCell(truncate(attempt.patchSummary || providerLabel(attempt.providerStatus), 80)),
      escapeTableCell(formatPaths(attempt.touchedPaths)),
      escapeTableCell(formatLocalTest(attempt)),
      escapeTableCell(formatOracle(attempt)),
      escapeTableCell(outcomeLabel(attempt.outcome))
    ].join(' | ').replace(/^/, '| ').replace(/$/, ' |')),
    ...(attempts.length > 10 ? [`| ... | ... | ... | ... | ... | ${attempts.length - 10} more attempt(s) omitted |`] : [])
  ];
}

function repairNextAction(summary: RepairSummary): string[] {
  const guidance = repairGuidance(summary);
  return [
    '## Next Action',
    '',
    `**What happened:** ${guidance.whatHappened}`,
    `**Next action:** ${guidance.nextAction}`,
    `**Done when:** ${guidance.doneWhen}`
  ];
}

function repairGuidance(summary: RepairSummary): {
  doneWhen: string;
  nextAction: string;
  whatHappened: string;
} {
  switch (summary.status) {
    case 'repaired':
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit after the repair commit.',
        nextAction: 'Review the repair commit and wait for preview to rerun on the updated PR branch.',
        whatHappened: 'Repair produced an implementation-only commit after the local oracle passed.'
      };
    case 'skipped':
      return {
        doneWhen: 'Repair is enabled and a repairable preview failure is available, or the preview passes without repair.',
        nextAction: 'Enable repair in `.postman-template/onboarding.yml` if automated implementation repair is expected.',
        whatHappened: 'Repair did not run for this PR.'
      };
    case 'blocked':
      return repairBlockedGuidance(summary.blockedReason);
    case 'failed':
    default:
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit.',
        nextAction: 'Inspect the repair logs and preview failure context, then rerun repair after fixing the blocking issue.',
        whatHappened: 'Repair failed before it could produce a verified implementation commit.'
      };
  }
}

function repairBlockedGuidance(reason: string | undefined): {
  doneWhen: string;
  nextAction: string;
  whatHappened: string;
} {
  switch (reason) {
    case 'budget_exhausted':
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit.',
        nextAction: 'Review the latest preview failures and repair summary, then fix manually or raise the repair attempt budget for another run.',
        whatHappened: 'Repair used all accepted attempts without producing a passing local TDD oracle.'
      };
    case 'owner_action_required':
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit after a manual fix.',
        nextAction: 'Inspect the escalation payload (models tried, last failure, run URL) in the repair summary, then fix the implementation manually.',
        whatHappened: 'Repair exhausted all attempts including the escalation model without producing a passing local TDD oracle.'
      };
    case 'repeated_failure':
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit after the failure is addressed.',
        nextAction: 'The same failure fingerprint recurred consecutively. Inspect the normalized error and fix the root cause rather than retrying.',
        whatHappened: 'Repair circuit breaker stopped the loop because the same failure recurred without progress.'
      };
    case 'fork_pr':
      return {
        doneWhen: 'A maintainer applies the implementation fix on a trusted branch and preview passes.',
        nextAction: 'Fix the PR manually or ask a maintainer to reproduce the change on a non-fork branch.',
        whatHappened: 'Automated repair is disabled for fork pull requests.'
      };
    case 'missing_failure_context':
    case 'missing_failure_commit':
    case 'malformed_failure_context':
    case 'stale_failure':
      return {
        doneWhen: 'A fresh `Postman TDD Preview` sticky comment exists for the latest PR head commit.',
        nextAction: 'Wait for preview to finish on the latest commit, then rerun repair if the failure is still repairable.',
        whatHappened: 'Repair could not trust the sticky preview failure context for the current PR head.'
      };
    case 'unsupported_failure_phase':
      return {
        doneWhen: '`Postman TDD Preview` passes after the non-repairable setup or contract-state issue is fixed.',
        nextAction: 'Fix the preview failure described in the `Postman TDD Preview` comment; this phase is not implementation-repairable.',
        whatHappened: 'The preview failed in a phase repair mode does not patch.'
      };
    case 'agent_blocked':
    case 'agent_no_change':
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit.',
        nextAction: 'Inspect the provider message and current failures, then fix manually or adjust repair context before rerunning.',
        whatHappened: 'The repair provider could not produce an applicable implementation patch.'
      };
    case 'tdd_disabled':
    case 'repair_disabled':
      return {
        doneWhen: 'TDD preview and repair are enabled, or preview passes without repair.',
        nextAction: 'Enable the relevant TDD repair settings if automated repair is desired.',
        whatHappened: 'Repair was skipped or blocked by repository configuration.'
      };
    default:
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit.',
        nextAction: 'Inspect the blocked reason and preview failure context, then rerun repair after resolving the issue.',
        whatHappened: 'Repair stopped before it could commit a verified implementation fix.'
      };
  }
}

function escapeTableCell(value: string): string {
  return value.replace(/\|/g, '\\|').replace(/\r?\n/g, ' ');
}

function formatLocalTest(attempt: RepairAttemptDiagnostic): string {
  const localTest = attempt.localTest;
  if (!localTest || localTest.status === 'skipped') return 'skipped';
  return localTest.status === 'passed'
    ? 'passed'
    : `failed${typeof localTest.exitCode === 'number' ? ` (${localTest.exitCode})` : ''}`;
}

function formatOracle(attempt: RepairAttemptDiagnostic): string {
  const oracle = attempt.oracle;
  if (!oracle || oracle.status === 'skipped') return 'skipped';
  if (oracle.status === 'passed') return 'passed';
  const phase = oracle.phase ? `${oracle.phase}` : 'failed';
  const count = typeof oracle.failureCount === 'number' ? `, ${oracle.failureCount} failure(s)` : '';
  return `${phase}${count}`;
}

function formatPaths(paths: string[]): string {
  if (paths.length === 0) return '(none)';
  if (paths.length <= 3) return paths.join(', ');
  return `${paths.slice(0, 3).join(', ')} +${paths.length - 3}`;
}

function outcomeLabel(outcome: RepairAttemptOutcome): string {
  switch (outcome) {
    case 'local_test_failed':
      return 'local test failed';
    case 'oracle_failed':
      return 'oracle failed';
    case 'oracle_passed':
      return 'oracle passed';
    case 'provider_blocked':
      return 'provider blocked';
    case 'provider_no_change':
      return 'provider no change';
  }
}

function providerLabel(status: RepairAttemptDiagnostic['providerStatus']): string {
  switch (status) {
    case 'blocked':
      return 'Provider reported blocked.';
    case 'changed':
      return 'Provider proposed a patch.';
    case 'no_change':
      return 'Provider reported no change.';
  }
}

function truncate(value: string, maxLength: number): string {
  const trimmed = value.trim();
  return trimmed.length > maxLength ? `${trimmed.slice(0, maxLength - 1)}...` : trimmed;
}

export function isRepairComment(body: string | undefined): boolean {
  return Boolean(body?.includes(REPAIR_MARKER_START));
}
