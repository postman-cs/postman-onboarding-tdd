import { mkdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

import type { RepairStatus } from '../types.js';

const REPAIR_MARKER_START = '<!-- postman-tdd-repair';
const REPAIR_MARKER_END = '-->';
const SUMMARY_DIR = '.postman-tdd';
const SUMMARY_PATH = join(SUMMARY_DIR, 'repair-summary.json');

export interface RepairSummary {
  attempts: number;
  blockedReason?: string;
  commitSha?: string;
  message: string;
  prNumber: number;
  schemaVersion: 1;
  status: RepairStatus;
}

export function writeRepairSummary(summary: RepairSummary): string {
  mkdirSync(SUMMARY_DIR, { recursive: true });
  writeFileSync(SUMMARY_PATH, `${JSON.stringify(summary, null, 2)}\n`, 'utf8');
  return SUMMARY_PATH;
}

export function renderRepairComment(summary: RepairSummary): string {
  const marker = `${REPAIR_MARKER_START}\n${JSON.stringify({ prNumber: summary.prNumber, schemaVersion: 1 })}\n${REPAIR_MARKER_END}`;
  const statusLabel = summary.status.toUpperCase();
  const lines = [
    marker,
    `# Postman TDD Repair (${statusLabel})`,
    '',
    `**Attempts:** ${summary.attempts}`,
    summary.commitSha ? `**Commit:** \`${summary.commitSha}\`` : '',
    summary.blockedReason ? `**Blocked reason:** \`${summary.blockedReason}\`` : '',
    '',
    summary.message,
    '',
    ...repairNextAction(summary),
    '',
    '_Generated by Postman Onboarding TDD Repair._'
  ].filter((line) => line !== '');
  return lines.join('\n');
}

function repairNextAction(summary: RepairSummary): string[] {
  const guidance = repairGuidance(summary);
  return [
    '## Next Action',
    '',
    `**What happened:** ${guidance.whatHappened}`,
    `**Next action:** ${guidance.nextAction}`,
    `**Done when:** ${guidance.doneWhen}`
  ];
}

function repairGuidance(summary: RepairSummary): {
  doneWhen: string;
  nextAction: string;
  whatHappened: string;
} {
  switch (summary.status) {
    case 'repaired':
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit after the repair commit.',
        nextAction: 'Review the repair commit and wait for preview to rerun on the updated PR branch.',
        whatHappened: 'Repair produced an implementation-only commit after the local oracle passed.'
      };
    case 'skipped':
      return {
        doneWhen: 'Repair is enabled and a repairable preview failure is available, or the preview passes without repair.',
        nextAction: 'Enable repair in `.postman-template/onboarding.yml` if automated implementation repair is expected.',
        whatHappened: 'Repair did not run for this PR.'
      };
    case 'blocked':
      return repairBlockedGuidance(summary.blockedReason);
    case 'failed':
    default:
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit.',
        nextAction: 'Inspect the repair logs and preview failure context, then rerun repair after fixing the blocking issue.',
        whatHappened: 'Repair failed before it could produce a verified implementation commit.'
      };
  }
}

function repairBlockedGuidance(reason: string | undefined): {
  doneWhen: string;
  nextAction: string;
  whatHappened: string;
} {
  switch (reason) {
    case 'budget_exhausted':
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit.',
        nextAction: 'Review the latest preview failures and repair summary, then fix manually or raise the repair attempt budget for another run.',
        whatHappened: 'Repair used all accepted attempts without producing a passing local TDD oracle.'
      };
    case 'fork_pr':
      return {
        doneWhen: 'A maintainer applies the implementation fix on a trusted branch and preview passes.',
        nextAction: 'Fix the PR manually or ask a maintainer to reproduce the change on a non-fork branch.',
        whatHappened: 'Automated repair is disabled for fork pull requests.'
      };
    case 'missing_failure_context':
    case 'missing_failure_commit':
    case 'malformed_failure_context':
    case 'stale_failure':
      return {
        doneWhen: 'A fresh `Postman TDD Preview` sticky comment exists for the latest PR head commit.',
        nextAction: 'Wait for preview to finish on the latest commit, then rerun repair if the failure is still repairable.',
        whatHappened: 'Repair could not trust the sticky preview failure context for the current PR head.'
      };
    case 'unsupported_failure_phase':
      return {
        doneWhen: '`Postman TDD Preview` passes after the non-repairable setup or contract-state issue is fixed.',
        nextAction: 'Fix the preview failure described in the `Postman TDD Preview` comment; this phase is not implementation-repairable.',
        whatHappened: 'The preview failed in a phase repair mode does not patch.'
      };
    case 'agent_blocked':
    case 'agent_no_change':
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit.',
        nextAction: 'Inspect the provider message and current failures, then fix manually or adjust repair context before rerunning.',
        whatHappened: 'The repair provider could not produce an applicable implementation patch.'
      };
    case 'tdd_disabled':
    case 'repair_disabled':
      return {
        doneWhen: 'TDD preview and repair are enabled, or preview passes without repair.',
        nextAction: 'Enable the relevant TDD repair settings if automated repair is desired.',
        whatHappened: 'Repair was skipped or blocked by repository configuration.'
      };
    default:
      return {
        doneWhen: '`Postman TDD Preview` passes on the latest PR head commit.',
        nextAction: 'Inspect the blocked reason and preview failure context, then rerun repair after resolving the issue.',
        whatHappened: 'Repair stopped before it could commit a verified implementation fix.'
      };
  }
}

export function isRepairComment(body: string | undefined): boolean {
  return Boolean(body?.includes(REPAIR_MARKER_START));
}
